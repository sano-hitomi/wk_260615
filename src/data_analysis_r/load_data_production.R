# 1. Load processed data
# -----------------------------------------------------------------------------
samplesheet <- read.csv(
  "data/production/processed/samplesheet.csv",
  stringsAsFactors = FALSE
)

feat_meta <- read.csv(
  "data/production/processed/feature_metadata.csv",
  stringsAsFactors = FALSE
)

feat_mat <- read.csv(
  "data/production/processed/feature_matrix.csv",
  row.names     = 1,
  check.names   = FALSE,
  stringsAsFactors = FALSE
)

cat("=== Data loaded ===\n")
cat(sprintf("samplesheet : %d samples x %d columns\n",
            nrow(samplesheet), ncol(samplesheet)))
cat(sprintf("feat_meta   : %d features x %d columns\n",
            nrow(feat_meta), ncol(feat_meta)))
cat(sprintf("feat_mat    : %d features x %d samples\n",
            nrow(feat_mat), ncol(feat_mat)))

# -----------------------------------------------------------------------------
# 1b. Merge MS-FINDER rank-1 annotations into feat_meta (if available)
#
#   Source : data/production/processed/feature_metadata_msfinder_2090.csv
#            (generated by src/python/annotate_unknown_msfinder.py)
#
#   For "Unknown" features that MS-FINDER successfully annotated, the following
#   columns are added to feat_meta (rank-1 / best-score candidate only):
#     MSFINDER_structure, MSFINDER_total_score, MSFINDER_formula,
#     MSFINDER_ontology, MSFINDER_inchikey, MSFINDER_smiles, MSFINDER_databases
#   A logical helper column MSFINDER_annotated is also set (TRUE / FALSE).
#
#   feat_meta_msfinder : full table including all candidate ranks (for reference)
# -----------------------------------------------------------------------------
MSFINDER_COLS <- c("MSFINDER_structure", "MSFINDER_total_score",
                   "MSFINDER_formula",   "MSFINDER_ontology",
                   "MSFINDER_inchikey",  "MSFINDER_smiles",
                   "MSFINDER_databases")
MSFINDER_CSV <- "data/production/processed/feature_metadata_msfinder_2090.csv"

if (file.exists(MSFINDER_CSV)) {
  feat_meta_msfinder <- read.csv(MSFINDER_CSV, stringsAsFactors = FALSE)

  # Extract rank-1 candidates only (one row per Alignment_ID)
  msfinder_rank1 <- feat_meta_msfinder[
    !is.na(feat_meta_msfinder$MSFINDER_rank) & feat_meta_msfinder$MSFINDER_rank == 1L,
    c("Alignment_ID", MSFINDER_COLS)
  ]

  # Merge into feat_meta
  feat_meta <- merge(feat_meta, msfinder_rank1, by = "Alignment_ID", all.x = TRUE)
  feat_meta$MSFINDER_annotated <- !is.na(feat_meta$MSFINDER_structure)

  n_annotated <- sum(feat_meta$MSFINDER_annotated, na.rm = TRUE)
  cat(sprintf("feat_meta + MSFINDER    : %d Unknown features newly annotated\n",
              n_annotated))
} else {
  # File not found — add empty columns so downstream scripts don't error
  for (col in c(MSFINDER_COLS, "MSFINDER_annotated")) {
    feat_meta[[col]] <- NA
  }
  feat_meta_msfinder <- NULL
  cat("MS-FINDER output not found — MSFINDER columns set to NA.\n")
  cat(sprintf("  Expected: %s\n", MSFINDER_CSV))
}

