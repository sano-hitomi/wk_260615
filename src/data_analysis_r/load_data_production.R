blk <- samplesheet[samplesheet$type == "BLK", ]

cat(sprintf("\nbio (biological only) : %d samples\n", nrow(bio)))
cat(sprintf("bio_all (+ rerun)     : %d samples\n", nrow(bio_all)))
cat(sprintf("qc                    : %d samples\n", nrow(qc)))
cat(sprintf("blk                   : %d samples\n", nrow(blk)))

# -----------------------------------------------------------------------------
# 3. Helper: extract a numeric sub-matrix
#    Usage: mat <- get_mat(bio$label)
# -----------------------------------------------------------------------------
get_mat <- function(labels) {
  cols <- intersect(labels, colnames(feat_mat))
  feat_mat[, cols, drop = FALSE]
}

